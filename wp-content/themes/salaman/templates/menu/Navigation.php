    $('.ul_hide > li > a').click(function () {
        if ($(this).next().is(":visible")) {
            $(this).next().hide();
        }
        else {
            $(this).next().toggle();
        }

    });

    $('.ul_hide > li > a > .ul_hide > li > a').click(function () {
        if ($(this).next().is(":visible")) {
            $(this).next().hide();
        }
        else {
            $(this).next().toggle();
        }

    });
